#include <bits/stdc++.h>
using namespace std;

#define ll long long
#define nl '\n'
#define FastIO                        \
    ios_base::sync_with_stdio(false); \
    cin.tie(0);                       \
    cout.tie(0);

void TC()
{
    int n;
    cin >> n;
    map<int, int> A;
    int mx = -1;
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        mx = max(mx, ++A[x]);
    }
    cout << n - mx << nl;
}

int main()
{
    FastIO;

    int t = 1;
    cin >> t;
    while (t--)
        TC();

    return 0;
}
