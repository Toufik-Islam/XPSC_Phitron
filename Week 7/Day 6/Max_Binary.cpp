#include <bits/stdc++.h>
using namespace std;

#define ll long long
#define nl '\n'
#define FastIO                        \
    ios_base::sync_with_stdio(false); \
    cin.tie(0);                       \
    cout.tie(0);

void TC()
{
    int n, k;
    string a;
    cin >> n >> k >> a;
    if (a[0] == '0')
        a[0] = '1', k--;
    while (k--)
        a += '0';
    cout << a << nl;
}

int main()
{
    FastIO;

    int t = 1;
    cin >> t;
    while (t--)
        TC();

    return 0;
}
