    cout.tie(0);

void solve()
{
    int n;
    cin >> n;
    int ans = n;
    for (int i = 1; i * i <= n; i++)
    {
        int mn = i - 1 + ((n - i) + i - 1) / i ;
        ans = min(ans, mn);
    }
    cout << ans << nl;
}

int main()
{
    Faster;

    int t = 1;
    cin >> t;
    while (t--)
        solve();

    return 0;
}
